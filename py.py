from flask import Flask
import random

app = Flask(__name__)

# --- Данные ---

facts_list = [
    "Средний пользователь проверяет смартфон более 150 раз в день.",
    "Феномен 'фантомной вибрации' испытывают около 90% владельцев смартфонов.",
    "Синий свет от экранов подавляет выработку мелатонина, ухудшая сон.",
    "Приложения соцсетей используют переменное подкрепление, как игровые автоматы.",
    "Многозадачность с гаджетами может снижать эффективность работы мозга.",
    "Термин 'номофобия' означает страх остаться без мобильного телефона.",
    "Уведомления вызывают выброс дофамина, создавая цикл зависимости."
]

predictions_list = [
    "Сегодня твой цифровой детокс будет успешным!",
    "Алгоритмы готовят для тебя что-то интересное.",
    "Отложи телефон на 15 минут, и мир заиграет новыми красками.",
    "Тебя ждет приятное сообщение от старого друга.",
    "Твоя батарея продержится дольше, чем ты думаешь.",
    "Сегодня отличный день, чтобы прочитать бумажную книгу.",
    "Wi-Fi сигнал будет стабильным весь день!"
]

# --- Маршруты ---

@app.route("/")
def index():
    # Главная страница
    return '''
    <h1>Добро пожаловать в мир цифрового детокса!</h1>
    <p>Выбери, куда хочешь отправиться:</p>
    <ul>
        <li><a href="/random_fact">Узнать случайный факт</a></li>
        <li><a href="/secret">🔮 Получить тайное предсказание</a></li>
    </ul>
    '''

@app.route("/random_fact")
def show_random_fact():
    # Страница с фактом
    random_fact = random.choice(facts_list)
    return f'''
    <h1>Вот что интересно знать:</h1>
    <p>{random_fact}</p>
    <br>
    <a href="/">⬅ Вернуться на главную</a> | 
    <a href="/secret">🔮 К предсказаниям</a>
    '''

@app.route("/secret")
def secret():
    # Тайная страница с предсказанием
    prediction = random.choice(predictions_list)
    return f'''
    <h1 style="color: purple;">🔮 Магический Шар Технологий</h1>
    <p style="font-size: 20px;"><i>"{prediction}"</i></p>
    <br>
    <a href="/">⬅ На главную</a> | 
    <a href="/random_fact">📚 Еще факт?</a>
    '''

app.run(debug=True)
